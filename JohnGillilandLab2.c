// main function, run after console reset
void main(void) {
  // set palette colors
  unsigned int x = 15;
  unsigned int t = 0;
  unsigned int cur_x = 64;
  unsigned int cur_y = 63;
  unsigned int limit_x = 240;
  bool scrolled = false;
  char pad;
  bool left = true;
  bool moved_x = false;
  bool moved_y = false;
  pal_col(0,0x1a);	// set screen to dark blue
  pal_col(1,0x0d);	// fuchsia
  pal_col(2,0x2c);	// grey
  pal_col(3,0x2a);	// white
  pal_col(17,0x30);
  pal_col(18,0x2c);
  pal_col(19,0x08);

  
  vram_adr(0x2000);
  vram_unrle(map1);
  vram_adr(0x2400);
  vram_unrle(map2);
  

  // enable PPU rendering (turn on screen)
  ppu_on_all();
  vrambuf_clear();
  set_vram_update(updbuf);
  // infinite loop
  while (1) {
    char cur_oam = 0;
    unsigned int i = 0;
    //pad = 0;
    pad = pad_poll(i);
    for(i = 0; i < 2; i++){
      // move actor[i] left/right
      if (pad&PAD_LEFT && cur_x>8 && !moved_x){ cur_x+=-8; moved_x = true;}
      else if (pad&PAD_RIGHT && cur_x<240 && !moved_x){ moved_x = true; cur_x+=8;}
      else if (pad&PAD_RIGHT && cur_x>=240 && !scrolled && !moved_x){
        scroll(256, 0);
        scrolled = true;
        moved_x = true;
        cur_x = 8;
      }
      else if (pad&PAD_LEFT && cur_x<=8 && scrolled){ 
        scroll(0, 0);
        scrolled = false;
        moved_x = true;
        cur_x = 240;
      }
      else if (!(pad&PAD_LEFT)&&!(pad&&PAD_RIGHT)) moved_x = false;
      //else cur_x=0;
      // move actor[i] up/down
      if (pad&PAD_UP && cur_y>8 && !moved_y){ cur_y+=-8; moved_y = true;}
      else if (pad&PAD_DOWN && cur_y<212 && !moved_y){ moved_y = true; cur_y+=8;}
      else if (!(pad&PAD_UP)&&!(pad&&PAD_DOWN)) moved_y = false;
      //else cur_y=0;
      //cur_oam = oam_spr(cur_x, cur_y, 0x90, 0x0, cur_oam);
    }
    cur_oam = oam_spr(cur_x, cur_y, 0x90, 0x0, cur_oam);
    cur_oam = oam_spr(40,7,0x31,0x0,cur_oam); 
    vrambuf_flush();
  }
}
